-- ====================================================================
-- 2. seats · lab members (≤ 15 rows; C9)
-- ====================================================================
-- Each row is a login identity bound to a lab. `role` gates admin
-- capabilities (topic CRUD, invite, export). `invite_token_hash` is
-- the sha256 of the one-time invite token emitted by admin; it is
-- cleared to null once consumed (enforced by T006 consumeInvite()).
-- `invite_expires_at` enforces the 24h TTL from spec.md §7.
-- ====================================================================
create table if not exists seats (
  id                    bigserial       primary key,
  lab_id                bigint          not null
                        references labs(id) on delete restrict,
  email                 text            not null,
  role                  text            not null,
  -- R1→R2 Q2 auth columns (T003 R2-a · pre-loaded here so T006
  -- does not need its own migration). `invite_token_hash` stores
  -- sha256(plain token); plain token exists only in the email/URL
  -- handed to the invited user. Nulls mean "no pending invite".
  invite_token_hash     text            null,
  invite_expires_at     timestamptz     null,
  invited_at            timestamptz     null,
  last_login_at         timestamptz     null,
  created_at            timestamptz     not null default now(),

  -- One identity per (lab, email) pair; O1/O3 queries dedupe by this.
  constraint seats_lab_email_unique       unique (lab_id, email),
  constraint seats_role_enum              check (role in ('admin', 'member'))
);

-- UNIQUE partial index on invite_token_hash for lookup + rejection
-- of duplicate unconsumed tokens (only enforced when token is set).
create unique index if not exists seats_invite_token_hash_uniq
  on seats (invite_token_hash)
  where invite_token_hash is not null;


-- ====================================================================
-- 3. sessions · login session store (JWT hash lookup + revocation)
-- ====================================================================
-- Each row tracks one successful login. `token_hash` is the sha256 of
-- the JWT (JWT itself stays in the httpOnly cookie, never in DB).
-- `login_date` is redundant (= issued_at::date) but kept as a covered
-- column on `(seat_id, login_date)` index for O1/O3 queries.
-- `revoked_at` enables server-side logout / admin-forced kick
-- (auth middleware asserts `revoked_at IS NULL`).
-- `last_active_at` is bumped on every authenticated request for
-- sliding expiry and bus-factor sentinel (BUS-1).
-- ====================================================================
create table if not exists sessions (
  id                    bigserial       primary key,
  seat_id               bigint          not null
                        references seats(id) on delete cascade,
  token_hash            text            not null,
  issued_at             timestamptz     not null default now(),
  expires_at            timestamptz     not null,
  login_date            date            not null, -- redundant for index
  ip                    text            null,
  user_agent            text            null,
  -- R1→R2 Q2 auth columns (T003 R2-a).
  revoked_at            timestamptz     null,
  last_active_at        timestamptz     not null default now(),

  constraint sessions_token_hash_uniq unique (token_hash)
);

-- Hot index for day-active queries (O1 · PI 25/30; O3 · operator ≥2/wk).
create index if not exists sessions_seat_login_date_idx
  on sessions (seat_id, login_date);

-- Used by operator-absence sentinel (BUS-1) and by session lookup.
create index if not exists sessions_seat_last_active_idx
  on sessions (seat_id, last_active_at);


