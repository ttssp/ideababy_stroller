-- ====================================================================
-- 14. paper_summaries · LLM-generated ≤ 3-sentence summary (R1 · D15)
-- ====================================================================
-- Derived-data table (distinct from llm_calls audit, see ADR-6).
-- Per (paper × topic × prompt_version) one row. `summary_text` is
-- the rendered ≤ 3-sentence summary shown in briefings. `llm_call_id`
-- FK preserves the audit trail back to the invoking LLM call.
--
-- RED LINE 2 mechanical enforcement (paper_summaries side; D15):
--   `summary_sentence_cap` CHECK counts sentence-terminal punctuation
--   directly (half-width `.!?` + full-width `。！？`) using
--   `regexp_matches(..., 'g')`; asserts 1..3 terminators. Counting
--   terminators (rather than splitting on whitespace) works for pure
--   Chinese text with no whitespace between sentences — the previous
--   split-on-whitespace approach silently passed 5-sentence pure
--   Chinese inputs as a single segment. See risks.md TECH-8 for the
--   corresponding "rejects unterminated text" risk and the
--   application-layer mitigation (`truncateTo3Sentences` in
--   `src/lib/llm/utils.ts` appends `。` defensively).
-- ====================================================================
create table if not exists paper_summaries (
  id                    bigserial       primary key,
  paper_id              bigint          not null
                        references papers(id) on delete cascade,
  topic_id              bigint          not null
                        references topics(id) on delete cascade,
  summary_text          text            not null,
  -- F5 (2026-04-24 · v0.2.2 pre-R_final hardening): llm_call_id is NULLABLE
  -- to support JSON export round-trip (option (a) in api-contracts.md §3.11
  -- Import round-trip notes). Exporter omits llm_calls (audit-internal),
  -- importer defaults to setting this FK to null rather than fabricating
  -- stub llm_calls rows. ON DELETE SET NULL preserves summaries if an
  -- llm_calls row is later purged for audit-retention reasons.
  llm_call_id           bigint          null
                        references llm_calls(id) on delete set null,
  model_name            text            not null,
  prompt_version        text            not null,
  created_at            timestamptz     not null default now(),

  -- RED LINE 2 · D15 (G1 fix 2026-04-24 · R_final B1):
  -- Count sentence-terminal punctuation directly (English `.!?` +
  -- Chinese `。！？`), require 1..3 terminators. The `regexp_matches`
  -- global-flag form returns one row per match; `array_length(...)`
  -- yields the match count. Rejects zero-terminator text (enforces
  -- explicit termination) and >3-terminator text. Whitespace is
  -- irrelevant to the count, so pure Chinese `中文。第二句。第三句。`
  -- (no spaces) is correctly judged as 3 sentences.
  constraint summary_sentence_cap check (
    coalesce(
      array_length(
        regexp_matches(summary_text, '[.!?。！？]', 'g'),
        1
      ),
      0
    ) between 1 and 3
  ),
  -- One summary per (paper, topic, prompt_version); a prompt bump
  -- allows historical summaries under the older version to coexist.
  constraint paper_summaries_unique_key
    unique (paper_id, topic_id, prompt_version)
);

create index if not exists paper_summaries_paper_idx
  on paper_summaries (paper_id);
create index if not exists paper_summaries_topic_idx
  on paper_summaries (topic_id);


