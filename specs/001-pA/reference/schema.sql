-- ====================================================================
-- 8. actions · 4-action records (per seat × per paper; D4 fixed set)
-- ====================================================================
-- Each row is one annotation by a seat on a paper. `action` is the
-- closed enum `{read_now, read_later, skip, breadcrumb}` (D4). `why`
-- is a short free-text explanation.
--
-- RED LINE 2 mechanical enforcement (Layer 1 of 3; D16, ADR-7):
--   constraint `skip_requires_why` rejects any insert where
--   action='skip' and the `why` field (after btrim) is shorter
--   than 5 characters. Layers 2 and 3 live in the API
--   (src/lib/actions/recordAction.ts) and UI
--   (src/components/skip-why-input.tsx) respectively — any single
--   layer being bypassed is still caught by the remaining layers.
-- ====================================================================
create table if not exists actions (
  id                    bigserial       primary key,
  seat_id               bigint          not null
                        references seats(id) on delete cascade,
  paper_id              bigint          not null
                        references papers(id) on delete cascade,
  action                text            not null,
  why                   text            null,
  created_at            timestamptz     not null default now(),

  constraint actions_action_enum
    check (action in ('read_now', 'read_later', 'skip', 'breadcrumb')),
  -- Max 280 chars on any why text (spec §IN-3 / §4.1).
  constraint actions_why_length
    check (why is null or char_length(why) <= 280),
  -- RED LINE 2 · D16 Layer 1: skip is only valid if why has ≥ 5
  -- non-whitespace characters. See ADR-7 for rationale, TECH-8/9
  -- for Chinese char-count edge cases.
  constraint skip_requires_why
    check (
      action != 'skip' or
      (why is not null and char_length(btrim(why)) >= 5)
    )
);

-- Hot path: O3/O4 per-seat action audits and /papers/:id/history.
create index if not exists actions_seat_created_idx
  on actions (seat_id, created_at);
create index if not exists actions_paper_idx
  on actions (paper_id);


