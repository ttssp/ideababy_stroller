create index if not exists papers_published_at_idx on papers (published_at);
create index if not exists papers_categories_gin
  on papers using gin (categories);


-- ====================================================================
-- 6. paper_citations · citation graph edges
-- ====================================================================
-- One row per (citing paper → cited arxiv_id). `cited_arxiv_id` is a
